package Control.classes;

import Control.annotations.Bean;

@Bean
public class Cat {
    private String name;

    private Integer age;

    public Cat() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }
}
